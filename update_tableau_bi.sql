**02**

**刀刃向内：**
**用 RFM 经营沙盘给一线团队“发牌”**

如果说行业沙盘是找增量，那第二个沙盘则是为了保存量。

有一年，遇到总部发起的断臂改革“阿波罗项目”，要求把所有续费售后老客户剥离。那个时候各大区都面临惨烈的新签保盘压力。

续费期一到，一线的销售就像是个无头苍蝇。面对手里几百个老客户，根本分不清重点。有些客户只是图新鲜买了一次套餐，销售死盯着他不放；有些大金主好几个月不充值了，销售却觉得“大老板肯定很忙，不敢去打扰”。

结果就是，优质老客流失，沉没成本徒增。

怎么办？我们重新改造了做传统电商的那个“RFM 模型”，首创了一套适用于本地黄页这套“双边市场”的评级系统。

把评价标准换成了：**充值近度(R) / 充值频度(F) / 充值额度(M)**。

把这套算法写进第二张看板：**RFM 精细化运营沙盘**。

这张沙盘有多狠？它把大区内数十万的留存客户瞬间切片。系统底层跑完，这其实就等同于系统在给一线的销售“发暗牌”。

这套系统一上线，过去那些粗放的管理考核全部失效。我们直接在沙盘上定下规矩，对不同的客户圈层实行差异化的 SOP：

比如，系统跑出来一个“高充值、低频度”的客户（充值近度较近，可能一次性买了一年的大额框架协议，但平时很少零星充值）。对于这类高净值客户，系统的动作指标要求销售做“深度客情维系”，定期上门做业务诊断，确保年底的大额续费不闪失。

再比如，跑出来一个“低充值、高频度”的客户（充值近度极近，每个月都充一两百块钱买曝光）。对于这类极其活跃但客单价不高的客户，系统提示销售的重点是“培训与 Upsell（向上销售）”，教他们怎么用好工具，适时推介更大的套餐包将他们转化为头部客户。

有了这套精密的沙盘推演和标准化漏斗控制，业务员不再像无头苍蝇一样乱撞，而是将核心精力倾斜到了最能产生价值的节点上。

最终，在最动荡的那一年，我们保住了东北大区全国续费第一的成绩。

